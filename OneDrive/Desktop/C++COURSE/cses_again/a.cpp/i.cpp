#include <bits/stdc++.h>
using namespace std;
using ll = long long;
using vl = vector<ll>;

#define fin(v, n) for(int i = 0; i < n; ++i) { ll x; cin >> x; v.push_back(x); }

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;
    while (t--) {
       auto solve = [&]{
         int n;
        cin >> n;
        vl a;
        fin(a, n);
        
       };
solve();
        
    }

    return 0;
}
